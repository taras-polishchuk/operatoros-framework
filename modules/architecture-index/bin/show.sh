#!/usr/bin/env bash
# architecture-index show — emit architecture.md + architecture-index.json.
set -euo pipefail

target="."
out=""

while [[ $# -gt 0 ]]; do
  case "$1" in
    --target) target="$2"; shift 2 ;;
    --out)    out="$2"; shift 2 ;;
    *)        echo "unknown arg: $1" >&2; exit 1 ;;
  esac
done

if [[ ! -d "$target" ]]; then
  echo "architecture-index: target directory does not exist: $target" >&2
  exit 1
fi

[[ -z "$out" ]] && out="${target}/state"

mkdir -p "$out"

# Top-level dirs
mapfile -t top_dirs < <(find "$target" -maxdepth 1 -mindepth 1 -type d -printf '%f\n' 2>/dev/null | sort)

# Build the architecture.md narrative
md="${out}/architecture.md"
{
  echo "# Architecture Index — $(basename "$target")"
  echo
  echo "_Auto-generated by architecture-index v1.0 (OperatorOS v0.8.0)._"
  echo
  echo "## Top-level organization"
  echo
  if [[ ${#top_dirs[@]} -eq 0 ]]; then
    echo "_Flat directory. No top-level grouping. Consider defining one._"
  else
    for d in "${top_dirs[@]}"; do
      echo "- \`${d}/\`"
    done
  fi
  echo
  echo "## Claim registry (canonical homes)"
  echo
  # Map canonical concepts to their homes per OperatorOS convention.
  if [[ -f "$target/operatoros.yaml" ]]; then
    echo "- **Workspace manifest**: \`operatoros.yaml\`"
  fi
  if [[ -d "$target/modules" ]]; then
    echo "- **Modules**: \`modules/\` (OperatorOS extension units)"
  fi
  if [[ -d "$target/presets" ]]; then
    echo "- **Presets**: \`presets/\` (workspace configuration blueprints)"
  fi
  if [[ -d "$target/state" ]]; then
    echo "- **State**: \`state/\` (durable workspace state — reports, validation, history)"
  fi
  if [[ -d "$target/schemas" ]]; then
    echo "- **Schemas**: \`schemas/\` (JSON-Schema definitions)"
  fi
  if [[ -d "$target/vault" ]]; then
    echo "- **Vault**: \`vault/\` (secrets, denylisted from export)"
  fi
  echo
  echo "_Validation: run \`operatoros run architecture-index validate\` to cross-check claims._"
} > "$md"

# Build the architecture-index.json (machine-readable companion)
json="${out}/architecture-index.json"
{
  printf '{\n'
  printf '  "generated_at": "%s",\n' "$(date -u +%Y-%m-%dT%H:%M:%SZ)"
  printf '  "target": "%s",\n' "$target"
  printf '  "top_level_dirs": ['
  first=1
  for d in "${top_dirs[@]}"; do
    if [[ $first -eq 1 ]]; then first=0; else printf ', '; fi
    printf '"%s"' "$d"
  done
  printf '],\n'
  printf '  "claims": [\n'
  first=1
  if [[ -f "$target/operatoros.yaml" ]]; then
    printf '    {"concept":"workspace-manifest","home":"operatoros.yaml"}'
    first=0
  fi
  if [[ -d "$target/modules" ]]; then
    [[ $first -eq 0 ]] && printf ',\n'
    printf '    {"concept":"modules","home":"modules/"}'
    first=0
  fi
  if [[ -d "$target/presets" ]]; then
    [[ $first -eq 0 ]] && printf ',\n'
    printf '    {"concept":"presets","home":"presets/"}'
    first=0
  fi
  if [[ -d "$target/state" ]]; then
    [[ $first -eq 0 ]] && printf ',\n'
    printf '    {"concept":"state","home":"state/"}'
    first=0
  fi
  if [[ -d "$target/schemas" ]]; then
    [[ $first -eq 0 ]] && printf ',\n'
    printf '    {"concept":"schemas","home":"schemas/"}'
    first=0
  fi
  if [[ -d "$target/vault" ]]; then
    [[ $first -eq 0 ]] && printf ',\n'
    printf '    {"concept":"vault","home":"vault/"}'
    first=0
  fi
  printf '\n  ]\n'
  printf '}\n'
} > "$json"

echo "wrote ${md}"
echo "wrote ${json}"