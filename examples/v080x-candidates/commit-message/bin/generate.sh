#!/usr/bin/env bash
# commit-message generate — Conventional Commits message from staged diff.
#
# Heuristic: based on file extensions + directories, suggest type + scope.
# Output is a multi-line commit message; user can edit before commit.
set -euo pipefail

target="."
type=""
scope=""
max_lines=5

while [[ $# -gt 0 ]]; do
  case "$1" in
    --target)     target="$2"; shift 2 ;;
    --type)       type="$2"; shift 2 ;;
    --scope)      scope="$2"; shift 2 ;;
    --max-lines)  max_lines="$2"; shift 2 ;;
    *) echo "unknown arg: $1" >&2; exit 1 ;;
  esac
done

if ! git -C "$target" rev-parse --git-dir >/dev/null 2>&1; then
  echo "commit-message: ${target} is not a git repository" >&2
  exit 1
fi

# Heuristic: detect type from changed files
changed="$(git -C "$target" diff --cached --name-only 2>/dev/null || true)"
if [[ -z "$changed" ]]; then
  echo "commit-message: no staged changes" >&2
  echo "  hint: git add <files>, then re-run" >&2
  exit 1
fi

if [[ -z "$type" ]]; then
  # Auto-detect
  if echo "$changed" | grep -qE '\.test\.ts$|__tests__/'; then
    type="test"
  elif echo "$changed" | grep -qE '^(README\.md|CHANGELOG\.md|methodology/|CONTRIBUTING\.md|docs/)'; then
    type="docs"
  elif echo "$changed" | grep -qE '^examples/'; then
    type="examples"
  elif echo "$changed" | grep -qE '^(package\.json|package-lock\.json|\.github/)'; then
    type="chore"
  elif echo "$changed" | grep -qE '^core/src/'; then
    type="feat"
  elif echo "$changed" | grep -qE '^modules/.*/bin/'; then
    type="feat"
  elif echo "$changed" | grep -qE '^schemas/'; then
    type="feat"
  else
    type="chore"
  fi
fi

if [[ -z "$scope" ]]; then
  # Auto-detect from first changed file
  first="$(echo "$changed" | head -1)"
  case "$first" in
    core/src/commands/inspect.ts) scope="inspect" ;;
    core/src/commands/init.ts)     scope="init" ;;
    modules/bootstrap-md/*)         scope="bootstrap-md" ;;
    modules/identity-md/*)          scope="identity-md" ;;
    modules/drift-detector/*)       scope="drift-detector" ;;
    modules/mission-runner/*)       scope="mission-runner" ;;
    modules/context-builder/*)      scope="context-builder" ;;
    modules/workspace-census/*)     scope="workspace-census" ;;
    modules/architecture-index/*)   scope="architecture-index" ;;
    modules/module-cookbook/*)      scope="module-cookbook" ;;
    modules/bootstrap-tier-refresh/*) scope="bootstrap-tier-refresh" ;;
    examples/*)                     scope="examples" ;;
    methodology/*)                  scope="methodology" ;;
    core/__tests__/*)               scope="core-tests" ;;
    core/src/*)                     scope="core" ;;
    *)                              scope="" ;;
  esac
fi

# Subject line
if [[ -n "$scope" ]]; then
  subject="${type}(${scope}): "
else
  subject="${type}: "
fi

# Detect action from changed files
case "$type" in
  feat|examples)
    action="add"
    ;;
  fix)
    action="fix"
    ;;
  docs)
    action="document"
    ;;
  test)
    action="test"
    ;;
  refactor)
    action="refactor"
    ;;
  chore)
    action="chore"
    ;;
  *)
    action="update"
    ;;
esac

# Generate subject (replace if user provided --subject; else default)
file_count="$(echo "$changed" | wc -l)"
subject+="${action} ${file_count} file(s)"

# Body
{
  echo "${subject}"
  echo
  echo "Files changed:"
  echo "$changed" | head -$max_lines | sed 's/^/- /'
  if [[ $file_count -gt $max_lines ]]; then
    echo "- ... and $((file_count - max_lines)) more"
  fi
  echo
  # Conventional Commits footer (auto-detected from repo)
  if git -C "$target" config --get user.email >/dev/null 2>&1; then
    echo "Generated by commit-message v1.0"
  fi
}