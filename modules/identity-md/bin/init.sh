#!/usr/bin/env bash
# identity-md init — consolidate latest interview answers into IDENTITY.md.
set -euo pipefail

target="."
while [[ $# -gt 0 ]]; do
  case "$1" in
    --target) target="$2"; shift 2 ;;
    *) echo "unknown arg: $1" >&2; exit 1 ;;
  esac
done

history="${target}/state/identity-md/interview-history.jsonl"
if [[ ! -f "$history" ]]; then
  echo "identity-md init: no interview history at ${history}" >&2
  echo "                       run \`operatoros run identity-md interview\` first." >&2
  exit 1
fi

# Last line of JSONL = latest interview
latest="$(tail -1 "$history")"

# Extract answers via grep (avoid jq dependency)
extract() {
  echo "$latest" | grep -oE "\"q${1}\":\"[^\"]*\"" | sed -E "s/\"q${1}\":\"([^\"]*)\"/\1/" || true
}

a1="$(extract 1)"
a2="$(extract 2)"
a3="$(extract 3)"
a4="$(extract 4)"
a5="$(extract 5)"

# Vault-leakage tick (I2): refuse to write if any answer matches a secrets pattern.
# Per I2: identity-md MUST NOT write paths matching **/secrets.* or vault/.
combined="$a1 $a2 $a3 $a4 $a5"
if echo "$combined" | grep -qE 'secrets\.[a-z]+|vault/|\.env|API[_-]?KEY=|PASSWORD='; then
  echo "identity-md init: refusing to write — answer contains potential secret pattern." >&2
  echo "                       Re-run interview without including credentials." >&2
  exit 1
fi

out="${target}/IDENTITY.md"
ts="$(date -u +%Y-%m-%dT%H:%M:%SZ)"

cat > "$out" <<EOF
# IDENTITY — $(basename "$target")

> Generated by identity-md v1.0 (OperatorOS v0.8.0) on ${ts}.
> Re-run \`operatoros run identity-md interview\` to update.

## 1. Who am I

${a1:-_(not provided)_}

## 2. What I care about

${a2:-_(not provided)_}

## 3. How I like to work

${a3:-_(not provided)_}

## 4. Current focus

${a4:-_(not provided)_}

## 5. What the AI should never do without asking

${a5:-_(not provided)_}

---

onboarding_complete: true
_last_updated: ${ts}
EOF

echo "identity-md: wrote ${out}"