#!/usr/bin/env bash
# context-builder inspect — Render an AI-context bundle.
#
# Reads the workspace (catalog, layout, key files) and emits a Markdown
# narrative of 800–1500 tokens. Deterministic; no network.
set -euo pipefail

target="."
format="md"
since=""

while [[ $# -gt 0 ]]; do
  case "$1" in
    --target) target="$2"; shift 2 ;;
    --format) format="$2"; shift 2 ;;
    --since)  since="$2"; shift 2 ;;
    *)        echo "unknown arg: $1" >&2; exit 1 ;;
  esac
done

if [[ ! -d "$target" ]]; then
  echo "context-builder: target directory does not exist: $target" >&2
  exit 1
fi

manifest_present=0
bootstrap_present=0
identity_present=0
identity_content=""
bootstrap_content=""

if [[ -f "$target/operatoros.yaml" ]]; then
  manifest_present=1
fi
if [[ -f "$target/bootstrap.md" ]]; then
  bootstrap_present=1
  bootstrap_content="$(cat "$target/bootstrap.md")"
fi
if [[ -f "$target/IDENTITY.md" ]]; then
  identity_present=1
  identity_content="$(cat "$target/IDENTITY.md")"
fi

top_dirs="$(find "$target" -maxdepth 1 -mindepth 1 -type d -printf '%f\n' 2>/dev/null | sort | head -10)"
top_files="$(find "$target" -maxdepth 1 -mindepth 1 -type f -printf '%f\n' 2>/dev/null | sort | head -10)"

# --since: compute changed files since ref (if it's a git repo).
changed_files=""
if [[ -n "$since" ]]; then
  if git -C "$target" rev-parse --git-dir >/dev/null 2>&1; then
    changed_files="$(git -C "$target" diff --name-only "$since" HEAD 2>/dev/null || true)"
  fi
fi

# Token budget: aim for 800–1500 tokens. Markdown is ~4 chars/token.
max_chars=6000

emit_md() {
  echo "# Context Bundle — ${target}"
  echo
  echo "_Auto-generated by context-builder v1.0 (OperatorOS v0.8.0)._"
  echo

  echo "## What this workspace is"
  echo
  if [[ $manifest_present -eq 1 ]]; then
    echo "An initialized OperatorOS workspace rooted at \`${target}\`. The manifest \`operatoros.yaml\` is the canonical entry point; this directory was bootstrapped with \`operatoros init\`."
  else
    echo "A directory at \`${target}\` that has not been initialized as an OperatorOS workspace. There is no \`operatoros.yaml\`."
  fi
  echo

  echo "## Top-level organization"
  echo
  if [[ -n "$top_dirs" ]]; then
    echo "Subdirectories:"
    for d in $top_dirs; do echo "- \`$d/\`"; done
  else
    echo "No subdirectories."
  fi
  echo
  if [[ -n "$top_files" ]]; then
    echo "Top-level files:"
    for f in $top_files; do echo "- \`$f\`"; done
  fi
  echo

  echo "## Always-read tier"
  echo
  if [[ $bootstrap_present -eq 1 ]]; then
    echo "A \`bootstrap.md\` is present. Excerpt:"
    echo
    echo '```'
    echo "$bootstrap_content" | head -30
    echo '```'
  else
    echo "No \`bootstrap.md\`. The AI has no documented always-read tier."
  fi
  echo

  echo "## Engineer identity"
  echo
  if [[ $identity_present -eq 1 ]]; then
    echo "An \`IDENTITY.md\` is present. Excerpt:"
    echo
    echo '```'
    echo "$identity_content" | head -30
    echo '```'
  else
    echo "No \`IDENTITY.md\`. The AI has no canonical engineer identity."
  fi
  echo

  if [[ -n "$changed_files" ]]; then
    echo "## Changes since ${since}"
    echo
    echo '```'
    echo "$changed_files"
    echo '```'
    echo
  fi

  echo "## How an AI should approach this workspace"
  echo
  if [[ $bootstrap_present -eq 1 && $identity_present -eq 1 ]]; then
    echo "Read \`bootstrap.md\` first, then \`IDENTITY.md\`, then proceed to read module manifests (\`modules/*/module.yaml\`) before performing any mutation. The methodology is documented and the AI should align with it."
  elif [[ $bootstrap_present -eq 1 ]]; then
    echo "Read \`bootstrap.md\` first. Engineer identity is undocumented; ask before mutating anything that touches the engineer's voice."
  else
    echo "There is no documented methodology. Operate conservatively: read-only first, propose changes before applying them."
  fi
}

emit_json() {
  printf '{"target":"%s","format":"json","manifest_present":%s,"bootstrap_present":%s,"identity_present":%s,"top_dirs":"%s","since":"%s","changed_files":"%s"}\n' \
    "$target" \
    "$manifest_present" \
    "$bootstrap_present" \
    "$identity_present" \
    "$(echo "$top_dirs" | tr '\n' ',')" \
    "$since" \
    "$(echo "$changed_files" | tr '\n' ',')"
}

case "$format" in
  md)   emit_md ;;
  json) emit_json ;;
  *)    echo "unknown format: $format" >&2; exit 1 ;;
esac