#!/usr/bin/env bash
# bootstrap-md render — produce bootstrap.md for the workspace.
#
# Reads operatoros.yaml + presets/<active>/preset.yaml to discover
# installed modules; emits a five-section bootstrap.md (per methodology/04).
# Atomic write: temp file → mv. History is appended to state/bootstrap-md/history.jsonl.
set -euo pipefail

# Default target = WORKSPACE_ROOT env (auto-injected by operatoros run);
# falls back to "." if neither flag nor env is set.
target="${WORKSPACE_ROOT:-.}"
out=""
no_backup=0

while [[ $# -gt 0 ]]; do
  case "$1" in
    --target)    target="$2"; shift 2 ;;
    --out)       out="$2"; shift 2 ;;
    --no-backup) no_backup=1; shift ;;
    *)           echo "unknown arg: $1" >&2; exit 1 ;;
  esac
done

if [[ -z "$out" ]]; then
  out="${target}/bootstrap.md"
fi

manifest="${target}/operatoros.yaml"
if [[ ! -f "$manifest" ]]; then
  echo "bootstrap-md: no operatoros.yaml at ${target}" >&2
  exit 1
fi

# Discover active preset + installed modules
# M3 fix: only indented list items under `modules:` block, not other YAML
# lists like `deny:`. We extract the `modules:` block first, then list
# items within it. Two-space indent is the convention in
# presets-canonical/personal/preset.yaml.
preset="$(grep -E '^preset:' "$manifest" | head -1 | sed -E 's/^preset:[[:space:]]*//' | tr -d '\"' || true)"
[[ -z "$preset" ]] && preset="personal"

# Extract just the `modules:` block (from "modules:" to next top-level key)
modules="$(awk '
  /^modules:[[:space:]]*$/ { in_block=1; next }
  in_block && /^[a-zA-Z]/ { in_block=0 }
  in_block && /^  - / { print }
' "${target}/presets/${preset}/preset.yaml" 2>/dev/null | head -20 || true)"
[[ -z "$modules" ]] && modules="- (none yet — install one with \`operatoros add <path>\`)"

today="$(date -u +%Y-%m-%d)"

# Backup existing bootstrap.md (per B2: write atomically with backup history)
state_dir="${target}/state/bootstrap-md"
mkdir -p "$state_dir"

if [[ -f "$out" && $no_backup -ne 1 ]]; then
  backup="${state_dir}/bootstrap.$(date -u +%Y%m%dT%H%M%SZ).bak"
  cp "$out" "$backup"
  echo "bootstrap-md: backed up existing to ${backup}"
fi

# Render to temp file, then mv atomically
tmp="$(mktemp "${out}.tmp.XXXX")"
trap 'rm -f "$tmp"' EXIT

cat > "$tmp" <<EOF
# Bootstrap — $(basename "$target")

> Generated by bootstrap-md v1.0 (OperatorOS v0.8.0) on ${today}.
> This is the canonical always-read tier for any AI agent entering this workspace.
> Re-run \`operatoros run bootstrap-md render\` after changing modules or preset.

## 1. Always read first

- \`IDENTITY.md\`: who owns this workspace.
- \`operatoros.yaml\`: the workspace manifest — active preset and installed modules.
- \`bootstrap.md\`: this file — how to navigate the workspace and when to ask.
- \`presets/${preset}/preset.yaml\`: which modules this workspace composes.

## 2. Read when relevant

- \`modules/<module>/module.yaml\`: read before running or modifying that module.
- \`state/\`: read only when a task needs current runtime state.
- \`schemas/README.md\`: read before authoring a new config or module.

## 3. Discover on demand

- \`modules/**/README.md\`: search when you need a module's usage but it's not in scope yet.
- \`presets/**/*.yaml\`: search when composing or comparing presets.

## 4. Never read

- \`vault/**\`: secrets. Never read, never print, never commit.
- \`state/**/*.log\`: runtime noise; ignore unless explicitly debugging.
- \`node_modules/**\`, \`dist/**\`, \`.git/**\`: dependencies and build output.

## 5. Onboarding

- If \`IDENTITY.md\` is missing or lacks \`onboarding_complete: true\`, run the interview
  before starting real work.
- Interview script: methodology/05-onboarding-interview.md (five questions).
- Write the answers to \`IDENTITY.md\` and set \`onboarding_complete: true\`.

---

## Installed modules (current preset: ${preset})

$(while IFS= read -r line; do
  [[ -z "$line" ]] && continue
  stripped="${line#  - }"
  printf -- '- %s\n' "$stripped"
done <<< "$modules")

_last_updated: ${today}
EOF

mv "$tmp" "$out"
trap - EXIT
echo "bootstrap-md: wrote ${out}"

# Append history (JSONL)
history="${state_dir}/history.jsonl"
ts="$(date -u +%Y-%m-%dT%H:%M:%SZ)"
echo "{\"ts\":\"${ts}\",\"target\":\"${target}\",\"out\":\"${out}\",\"preset\":\"${preset}\"}" >> "$history"