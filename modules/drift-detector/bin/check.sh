#!/usr/bin/env bash
# drift-detector check — run all six per-principle checks.
set -euo pipefail

target="."
format="md"
strict=0

while [[ $# -gt 0 ]]; do
  case "$1" in
    --target) target="$2"; shift 2 ;;
    --format) format="$2"; shift 2 ;;
    --strict) strict=1; shift ;;
    *) echo "unknown arg: $1" >&2; exit 1 ;;
  esac
done

state_dir="${target}/state/drift-detector"
mkdir -p "$state_dir"
report="${state_dir}/drift-report-$(date -u +%Y%m%dT%H%M%SZ).md"

principles=(
  "single-authority:Single Authority"
  "everything-replaceable:Everything Replaceable"
  "typed-substrate:Typed Substrate"
  "composable:Composable"
  "evidence-based:Evidence-Based"
  "local-first:Local-First"
)

violations=0
reviews=0

{
  echo "# Drift Report — ${target}"
  echo
  echo "_Generated by drift-detector v1.0 (OperatorOS v0.8.0) on $(date -u +%Y-%m-%dT%H:%M:%SZ)._"
  echo
  echo "## Per-principle findings"
  echo
  for entry in "${principles[@]}"; do
    script="${entry%%:*}"
    label="${entry#*:}"
    echo "### ${label}"
    echo
    out="$(bash "${target}/modules/drift-detector/principles/${script}.sh" "$target" 2>&1 || true)"
    echo "$out"
    if echo "$out" | grep -q "VIOLATION"; then
      violations=$((violations + 1))
    elif echo "$out" | grep -q "REVIEW"; then
      reviews=$((reviews + 1))
    fi
    echo
  done

  echo "## Summary"
  echo
  echo "- VIOLATIONS: ${violations}"
  echo "- REVIEWS: ${reviews}"
  echo "- OK: $((6 - violations - reviews))"
} | tee "$report"

echo
echo "report: ${report}"

if [[ $strict -eq 1 && $violations -gt 0 ]]; then
  echo "strict mode: ${violations} violation(s) — exit 1"
  exit 1
fi